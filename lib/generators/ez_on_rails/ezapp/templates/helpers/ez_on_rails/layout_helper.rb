# frozen_string_literal: true

# Module to get some design related information to render the layout.
module EzOnRails::LayoutHelper
  # Returns the color of the background in the header area.
  def header_background_color
    '#000000'
  end

  # Returns the color of the text in the header area.
  def header_text_color
    '#ffffff'
  end

  # Returns the color of the background in the footer area.
  def footer_background_color
    '#000000'
  end

  # Returns the color of the text in the footer area.
  def footer_text_color
    '#ffffff'
  end
end
